public class JdAddress implements Serializable {

    /**
     *Comment for <code>serialVersionUID</code>
     */
    private static final long serialVersionUID = 1L;
    
    private String nation; //国家
    private Integer nationId;//国家编码
    private String province; //省
    private Integer provinceId;//省编码
    private String city; //市
    private Integer cityId;//市编码
    private String county; //县
    private Integer countyId;//县编码
    private String town; //城镇
    private Integer townId;//城镇编码
    
    public String getNation() {
        return nation;
    }
    public void setNation(String nation) {
        this.nation = nation;
    }
    public Integer getNationId() {
        return nationId;
    }
    public void setNationId(Integer nationId) {
        this.nationId = nationId;
    }
    public String getProvince() {
        return province;
    }
    public void setProvince(String province) {
        this.province = province;
    }
    public Integer getProvinceId() {
        return provinceId;
    }
    public void setProvinceId(Integer provinceId) {
        this.provinceId = provinceId;
    }
    public String getCity() {
        return city;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public Integer getCityId() {
        return cityId;
    }
    public void setCityId(Integer cityId) {
        this.cityId = cityId;
    }
    public String getCounty() {
        return county;
    }
    public void setCounty(String county) {
        this.county = county;
    }
    public Integer getCountyId() {
        return countyId;
    }
    public void setCountyId(Integer countyId) {
        this.countyId = countyId;
    }
    public String getTown() {
        return town;
    }
    public void setTown(String town) {
        this.town = town;
    }
    public Integer getTownId() {
        return townId;
    }
    public void setTownId(Integer townId) {
        this.townId = townId;
    }
}
